                                        <h5 style="text-align: justify;">Rules And Regulations:</h5>
                                        <br/>
                                        <p style="text-align: justify;"><strong>Event Format-</strong></p>
                                        <p style="text-align: justify;">In the solo (1 vs. 1) battles, all invited contestants are given 2 minutes to perform a showcase, where they are pitted up and ranked against all other showcases.</p>
                                        <p style="text-align: justify;"><strong>Judging Criteria | Standard</strong></p>
                                        <p style="text-align: justify;">The four criteria include: originality, musicality, technicality, and showmanship.</p>
                                        <ol style="text-align: justify;list-style-type: decimal;margin-left: 50px;margin-top: 10px;">
                                            <li>Each criteria should be graded on a 1-10 scale per beatboxer per battle round. The total points from both rounds will be counted towards the final score.</li>
                                            <li>Originality: New, creative, innovative, thoughtful, bold, adventurous, story-telling</li>
                                            <li>Musicality: Tone, volume, tempo, musical elements, consistent theme, flow</li>
                                            <li>Technicality: Mechanical ability, mic control, complicated structures and time signatures</li>
                                            <li>Showmanship: Well-rehearsed, clinical execution, entertaining, confidence, battle mentality</li>
                                        </ol>
                                        <p style="text-align: justify;"><strong>Battle Rules | Standard</strong></p>
                                        <ol style="text-align: justify;list-style-type: decimal;margin-left: 50px;margin-top: 10px;">
                                            <li>Performed in one single take</li>
                                            <li>2 minutes to perform a showcase in the elimination round and will be cut off exactly at the 2 minute mark</li>
                                            <li>Beatboxers will receive a 10 second warning</li>
                                            <li>Beatboxers may end the round early, but may face point deductions</li>
                                            <li>Thus, 1st place will battle 16th seed, 2nd place will battle 15th, so on and so forth</li>
                                            <li>If two showcases are equal on points, an impromptu battle will take place for the last spot.</li>
                                        </ol>
                                        <p style="text-align: justify;"><strong>Battle Rules for Solo&rsquo;s:</strong></p>
                                        <ol style="text-align: justify;list-style-type: decimal;margin-left: 50px;margin-top: 10px;">
                                            <li>Battles will be held in ABAB format</li>
                                            <li>The winner of the coin toss may choose who goes first</li>
                                            <li>If two microphones are used, they will be at the same volume and presets</li>
                                            <li>A draw will result in one 30-second overtime round where the 2nd contestant goes first</li>
                                        </ol>
                                        <p style="text-align: justify;"><strong>Battle Etiquette | Standard</strong></p>
                                        <p style="text-align: justify;">There are several unspoken rules that battlers should adhere to:</p>
                                        <ol style="text-align: justify;list-style-type: decimal;margin-left: 50px;margin-top: 10px;">
                                            <li>Always be respectful
                                                <br />
                                                <p>The value of respect sits at the foundation of beatbox and seeps into all the other rules. Battles can get heated, but it&rsquo;s important to express ourselves through the creation of music and artistic expression, rather than hateful words and racial slurs.</p>
                                                <p>A hug, handshake, or a high-five after the battle also goes a long way, showing that the battle is over and sets an example for those watching online. Good vibes only!</p>
                                            </li>
                                            <li>Be quiet during opponent&rsquo;s round
                                                <br />
                                                <p>Being silent during the opponent&rsquo;s round is the greatest sign of respect for the art. It&rsquo;s okay to joke around and react, but blatantly obstructing and interrupting the opponent&rsquo;s round is a low blow and will easily lose you the battle.</p>
                                            </li>
                                            <li>No physical contact
                                                <br />
                                                <p>Pretty simple: don&rsquo;t touch the opponent in a battle. It&rsquo;s not only distracting, but can also lead to further endangerment. Any malicious behavior like this can lead to disqualification from the entire event</p>
                                            </li>
                                            <li>Face each other
                                                <br />
                                                <p>A battle is a competition between two competitors, which means you have to show competitive spirit by facing each other and treating it as a battle, not two separate showcases.</p>
                                            </li>
                                            <li>Leave battles at battles
                                                <br />
                                                <p>Battles are only a small portion of beatbox culture. What makes the beatbox family great is the community, which shouldn&rsquo;t be tarnished by battles. A battle is not an absolute determination of your skill and ability as an artist, so remember to practice hard but also have fun.</p>
                                            </li>
                                        </ol>

